package com.ogiraffers.section03.increment;

public class Application01
{
    public static void main(String[] args)
    {
        /*
        증감 연산자
        피연산자의 앞 or 뒤에 사용이 가능하다.
        "++" : 1의 증가를 의미
        "--" : 1의 감소를 의미
         */

        int num =0;
        System.out.println(num);
        System.out.println(num++);
        System.out.println(num);


        System.out.println(num);
        System.out.println(++num);




    }

}
